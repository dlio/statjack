# 1000000 => 1,000,000 and $1000 => 1,000
  def commatize(number, delimiter = ',')
    number.to_s.reverse.gsub(%r{([0-9]{3}(?=([0-9])))}, "\\1#{delimiter}").reverse
  end
  

