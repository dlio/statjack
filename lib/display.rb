# colorization of console output

  def colorize(text, color_code)
    return "\e[#{color_code}m#{text}\e[0m"
  end

  def colorize_blue(text)
    return colorize(text,"1;36;40")
  end

  def colorize_title(text)
    return colorize(text,"1;32;40")
  end

  def colorize_red(text)
    return colorize(text,"1;31;40")
  end

  def colorize_white(text)
    return colorize(text,"1;37;40")
  end

  def colorize_green(text)
    return colorize(text,"1;32;40")
  end

  
